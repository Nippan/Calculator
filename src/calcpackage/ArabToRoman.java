package calcpackage;

public class ArabToRoman {
	public String arabToRoman(int input) {
		String s = "";
		
		if (input < 0) {
	        input *= -1;
	        s += "-";
	    }
		
	    while (input >= 10) {
	        s += "X";
	        input -= 10;
	    }
	    while (input >= 9) {
	        s += "IX";
	        input -= 9;
	    }
	    while (input >= 5) {
	        s += "V";
	        input -= 5;
	    }
	    while (input >= 4) {
	        s += "IV";
	        input -= 4;
	    }
	    while (input >= 1) {
	        s += "I";
	        input -= 1;
	    }    
	    return s;
	}
}
